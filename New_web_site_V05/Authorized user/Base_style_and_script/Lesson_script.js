$(document).ready(function(){
	$("#all_text_open").click(function(){
		$("#all_text_open").toggle();
		$(".lesson").toggle();
		$(".space_height").toggle();
		$("#test1").toggle();
	})
})

$(document).ready(function(){
	$("#all_text_close").click(function(){
		$("#all_text_open").toggle();
		$(".lesson").toggle();
		$(".space_height").toggle();
		$("#test1").toggle();
	})
})