$(document).ready(function(){
	$("#menu_button_open").click(function(){
		$(".negativ_bar").toggle();
		$("#menu_button_open").toggle();
		$("#menu_button_close").toggle();
	})
})

$(document).ready(function(){
	$("#menu_button_close").click(function(){
		$(".negativ_bar").toggle();
		$("#menu_button_open").toggle();
		$("#menu_button_close").toggle();
	})
})